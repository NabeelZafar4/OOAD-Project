package SimpleClasses;

public class Course {

	private String Name;
	private int CourseCode;
	public String getName() {
		return Name;
	}
	public void setName(String name) {
		Name = name;
	}
	public int getCourseCode() {
		return CourseCode;
	}
	public void setCourseCode(int courseCode) {
		CourseCode = courseCode;
	}
	
}
